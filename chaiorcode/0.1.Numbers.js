//Write a program to find the exponential of a given number.

function calculateExponential(base, exponent) {
    return Math.pow(base, exponent);
}

// Example usage:
let base = 2;
let exponent = 3;
let result = calculateExponential(base, exponent);
console.log(`The exponential of ${base} raised to the power of ${exponent} is: ${result}`);


